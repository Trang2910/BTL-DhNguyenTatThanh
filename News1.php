        <p style="font-size:15px"> <b> Chương trình đào tạo ưu việt </b></p>
        <p style="text-align: justify;font-size:15px">Chương trình đào tạo thạc sĩ tại trường ĐH Nguyễn Tất Thành được thiết kế theo hướng hoàn thiện, nâng cao kiến thức chuyên môn, đi sâu vào chuyên ngành ở khía cạnh quản lý cấp cao và dựa theo kết quả khảo sát ý kiến của các bên liên quan (doanh nghiệp, nhà tuyển dụng,..), tham khảo chương trình đào tạo của các trường trong và ngoài nước nhằm đáp ứng nhu cầu của thị trường lao động. Ngoài ra, chương trình đào tạo thạc sĩ của NTTU cũng mang tính đa năng cung cấp kiến thức để học viên sau khi tốt nghiệp có thể đảm đương nhiều vị trí khác nhau trong cơ quan, tổ chức. Trên cơ sở kiến thức được trang bị sẽ giúp học viên nâng cao khả năng tư duy tự nghiên cứu, giải quyết vấn đề ở tầm vĩ mô; phân tích thông tin để làm rõ các kết quả kinh doanh, đầu tư tài chính cũng như đưa ra các chiến lược phát triển hiệu quả nhất trong môi trường kinh doanh cạnh tranh hiện nay.<br>
                                        Bên cạnh đó,&nbsp; người học còn có cơ hội phát triển nhiều kỹ năng mềm khác như giao tiếp cá nhân và nhóm, quản trị bản thân, quản lý thời gian, xử lý vấn đề nảy sinh trong công việc,..</p>
        <p style="font-size:15px"> <b>Đội ngũ giảng viên giỏi chuyên môn </b>  </p>
        <p style="text-align: justify;font-size:15px">Tham gia công tác giảng dạy tại Trường ĐH Nguyễn Tất Thành là đội ngũ các giáo sư, tiến sĩ đầu ngành, có trình độ chuyên môn cao, tâm huyết với nghề. Đặc biệt có một số giảng viên được đào tạo tại các trường danh tiếng ở nước ngoài. Bên cạnh đó, Nhà trường còn có đội ngũ giảng viên là các doanh nhân, các CEO, những chuyên gia có kinh nghiệm, có năng lực sư phạm của các doanh nghiệp, các tập đoàn đa quốc gia trực tiếp giảng dạy. &nbsp;</p>
        <p style="text-align: center;"><img alt="" src="http://ntt.edu.vn/web/upload/images/32313334468_257a6c93e6_o.jpg" style="width:100%" width = "800px" height = "534px" ></p>
        <p style="text-align: center;">Đội ngũ giảng viên giỏi chuyên môn, giài kinh nghiệm đến từ các doanh nghiệp trực tiếp giảng dạy</p>
        <p style="font-size:15px"><b>Thời gian học tập linh hoạt dành cho người đi làm</b></p>
        <p style="text-align: justify;font-size:15px">Với mục tiêu tạo điều kiện học tập thuận lợi nhất cho học viên có thể dung hòa tốt giữa học tập và làm việc, ĐH Nguyễn Tất Thành xây dựng chương trình đào tạo theo học chế tín chỉ cùng mức học phí phù hợp. Bên cạnh đó, việc học từ 1,5 – 2 năm, tập trung các buổi tối và ngày thứ Bảy, Chủ nhật, học viên cao học các ngành Quản trị kinh doanh (QTKD), Tài chính – Ngân hàng (TCNH) và Công nghệ thông tin (CNTT) tại NTTU  giúp các học viên có thể đồng thời từng bước tham gia giảng dạy sinh viên. Cánh cửa này mang lại cho học viên cơ hội trở thành giảng viên đại học chuyên nghiệp trong tương lai nếu muốn.</p>
        <p style="text-align: justify;font-size:15px">Với những ưu thế trên, người học hoàn toàn yên tâm với sự lựa chọn của mình, nỗ lực nâng cấp triển vọng nghề nghiệp và khẳng định giá trị bản thân ở một tầm cao mới. </p>
