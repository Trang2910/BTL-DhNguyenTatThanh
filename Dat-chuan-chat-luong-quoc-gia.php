        <h3 class="" style="color: #024282 ">Đạt chuẩn chất lượng quốc gia</h3>
        <p>Chủ nhật, Ngày 19 tháng 02 năm 2017 2:00 pm</p>
        <hr>
        
        <p id="content1">Sáng ngày 06/05/2017, tại Trường ĐH Nguyễn Tất Thành, cơ sở 300A, Nguyễn Tất Thành, P.13, Q. 4, TP. HCM, Bộ GD&ĐT đã công bố và trao Giấy chứng nhận đạt chuẩn chất lượng cấp cơ sở giáo dục cho nhà trường. Buổi lễ có sự tham gia của GS.TSKH. Bùi Văn Ga, Thứ trưởng Bộ GD&ĐT; PGS.TS. Mai Văn Trinh, Cục trưởng Cục Khảo thí và Kiểm định chất lượng giáo dục; ông Trần Văn Nghĩa, Phó cục trưởng Cục Khảo thí và Kiểm định chất lượng giáo dục; ông Trần Anh Tuấn, Phó Vụ trưởng Vụ Giáo dục Đại học, Bộ GD&ĐT; ông Nguyễn Như Chinh, Vụ trưởng Vụ Thi đua – Khen thưởng Bộ công thương; ông Tống Ngọc Trung, Phó Cục trưởng Cục công tác phía Nam, Bộ Công thương; TS. Nguyễn Đức Nghĩa, PGĐ Đại học Quốc gia TP. HCM; TS Nguyễn Quốc Chính, PGĐ Trung tâm Kiểm định Chất lượng giáo dục, Đại học Quốc gia TP. HCM; ông Phạm Ngọc Thanh, PGĐ Sở GD&ĐT TP. HCM… cùng nhiều cán bộ, đại diện trong ngành giáo dục.</p>
        
        <p id="img">
        <img alt="" src="http://ntt.edu.vn/web/upload/images/Tin_Tuc/201704_tin/trao%20kiem%20dinh%20chat%20luong%20dai%20hoc%20nguyen%20tat%20thanh_ep.JPG" style="width: 100%" width = "800px" height = "580px"></p>
        
        <p id="content2" ><em><span >TS. Nguyễn Quốc Chính, PGĐ Trung tâm Kiểm định Chất lượng giáo dục ĐHQG HCM, trao chứng nhận kiểm định chất lượng giáo dục cho PGS.TS. Nguyễn Mạnh Hùng, hiệu trưởng trường ĐH NTT</span></span></em></p>
        
        <p id="content3" >Chứng nhận đạt chuẩn chất lượng giáo dục chính là thành quả mà trường ĐH Nguyễn Tất Thành đã đạt được sau thời gian dài nỗ lực và kiện toàn về chất lượng giáo dục. Nhà trường đã tự đánh giá cấp Chương trình đào tạo và cấp Cơ sở đào tạo (tức cấp Trường) theo Bộ tiêu chuẩn chất lượng của Bộ GD&ĐT. ĐH Nguyễn Tất Thành cũng thông qua các Bộ tiêu chuẩn kiểm định chất lượng (bao gồm cấp Trường lẫn cấp Chương trình đào tạo, của Bộ GD&ĐT cũng như AUN-QA) để làm cơ sở “tự điều chỉnh” các hoạt động trong nhà trường và hướng đến “đạt chuẩn” tất cả các lĩnh vực trong trường đại học gồm: đào tạo, nghiên cứu khoa học, phục vụ cộng đồng, đội ngũ, cơ sở vật chất… xây dựng thành công hệ thống đảm bảo chất lượng bên trong của trường.</p>
        
        <p id="content3" >Từ những thay đổi về lượng và chất, tháng 10/2016 nhà trường đã đăng ký đánh giá ngoài chính thức cấp cơ sở giáo dục với Trung tâm Kiểm định chất lượng giáo dục, ĐHQG TP. HCM và đã nghiêm túc tuân thủ quy trình đánh giá chặt chẽ của Trung tâm Kiểm định chất lượng giáo dục, cũng như hoàn thành công tác đánh giá ngoài theo Bộ tiêu chuẩn đánh giá chất lượng giáo dục trường đại học (giai đoạn khảo sát sơ bộ từ ngày 16-17/11/2016; giai đoạn khảo sát chính thức từ ngày 20-24/12/2016). Đến ngày 30/3/2017, Hội đồng Kiểm định chất lượng giáo dục, ĐHQG HCM đã thông qua và công bố Nghị quyết số 06/NQ-HĐKĐCLGD về việc thẩm định kết quả đánh giá chất lượng giáo dục, công nhận trường ĐH Nguyễn Tất Thành đạt chuẩn chất lượng cấp cơ sở giáo dục.</p>
        
        <p id="img">
        <img alt="" src="http://ntt.edu.vn/web/upload/images/Tin_Tuc/201704_tin/nguyen%20duc%20nghia%20trao%20chung%20nhan%20dai%20hoc%20nguyen%20tat%20thanh_ex-001.JPG" style="width: 100%" width = "800px" height = "450px"></p>
        
        <p id="content2" ><span ><em>
                <span style="font-size:12px;">TS. Nguyễn Đức Nghĩa, Phó Giám đốc ĐHQG TP. HCM,&nbsp;Phó chủ tịch Hiệp hội các trường ĐH–CĐ Việt Nam phát biểu, nhận xét về công tác kiểm định chất lượng giáo dục và thành tựu của Nhà trường</span></em></span></p>
        
        <p id="content3" >Là người đồng hành cùng ĐH Nguyễn Tất Thành trong suốt thời gian thực hiện công tác kiểm định chất lượng, TS. Nguyễn Đức Nghĩa, PGĐ Đại học Quốc gia TP. HCM, Phó chủ tịch Hiệp hội các trường đại học – cao đẳng Việt Nam, cho biết: “Tôi đánh giá cao việc làm và những thành tựu của nhà trường. Là một trường đại học ngoài công lập còn non trẻ nhưng ngay từ đầu đã thực hiện chất lượng giáo dục bằng cách mạnh dạn đầu tư vào chương trình đào tạo, đội ngũ giảng viên, cơ sở vật chất, dùng các Bộ tiêu chuẩn chất lượng để tự đánh giá, mời các tổ chức trong và ngoài nước đánh giá. Đây là con đường đúng đắn của ĐH Nguyễn Tất Thành, không chỉ để chuẩn hóa các hoạt động trong trường mà còn góp phần xóa nhà ranh giới giữa trường đại công và tư trong hệ thống giáo dục đại học Việt Nam, hướng đến hội nhập quốc tế”.</p>
        
        <p id="img"><img alt="" src="http://ntt.edu.vn/web/upload/images/Tin_Tuc/201704_tin/hieu%20truong%20nguyen%20manh%20hung%20dai%20hoc%20nguyen%20tat%20thanh_ex.JPG" style="width: 100%" width = "800px" height = "580px"></p>
        
        <p id="content2" ><em>PGS.TS. Nguyễn Mạnh Hùng chia sẻ về công tác kiểm định chất lượng giáo dục của ĐH NTT, bày tỏ sự vui mừng vì thành tựu mà trường đã đạt được</em></p>
        
        <p id="content3">Để đạt được các tiêu chí kiểm định này, ĐH Nguyễn Tất Thành đã đẩy mạnh đầu tư để đảm bảo chất lượng, nỗ lực đổi mới công tác đào tạo, hợp tác quốc tế... trong suốt 18 hình thành và phát triển để tạo dựng thương hiệu riêng cho trường. Đặc biệt, trong năm nay, trường vinh dự thuộc top những trường đại học có số lượng thí sinh đăng ký xét tuyển cao nhất. Đây chính là kết quả của sự nỗ lực bền bỉ của tập thể lãnh đạo trường ĐH Nguyễn Tất Thành và buổi lễ trao Giấy chứng nhận đạt chuẩn chất lượng giáo dục chính là sự khẳng định chính thức về chất lượng giáo dục của Nhà trường.</p>
        
        <p id="img"><img alt="" src="http://ntt.edu.vn/web/upload/images/Tin_Tuc/201704_tin/Bui%20Van%20Ga%20phat%20bieu%20dai%20hoc%20nguyen%20tat%20thanh_ex.JPG" style="width: 100%" width = "800px" height = "534px" ></p>
        
        <p id="content2"><em><span style="color:#808080;"><span style="font-size:12px;">GS.TSKH. Bùi Văn Ga, Thứ trưởng Bộ GD&amp;ĐT, đánh giá về hành trình kiểm định chất lượng giáo dục của ĐH NTT</span></span></em></p>
       
        <p id="content3" >“Sự kiện ngày hôm nay có sức lan tỏa rất tích cực trong khi cả hệ thống giáo dục đại học đang đẩy mạnh công tác đảm bảo và kiểm định chất lượng giáo dục nhằm nâng cao chất lượng đào tạo, góp phần tăng cường nguồn nhân lực có chất lượng cao, một trong những khâu đột phá để phát triển đất nước trong bối cảnh hội nhập quốc tế… Tôi tin tưởng và kỳ vọng rằng sự kiện công nhận đạt chuẩn chất lượng hôm nay mới chỉ là bước khởi đầu để trường ĐH Nguyễn Tất Thành tiến xa hơn trên con đường đảm bảo chất lượng đạt chuẩn khu vực và thế giới”, GS.TSKH. Bùi Văn Ga, Thứ trưởng Bộ GD&ĐT, chia sẻ.</p>
        
        <p id="content3">Việc được công nhận đạt chuẩn chất lượng giáo dục lần này mang ý nghĩa rất lớn đối với Đại học Nguyễn Tất Thành, là cột mốc quan trọng khẳng định thêm uy tín của Nhà trường với người học và xã hội trước sứ mệnh bồi dưỡng nhân tài, đào tạo ra nguồn lực đáp ứng nhu cầu của xã hội.</p>


