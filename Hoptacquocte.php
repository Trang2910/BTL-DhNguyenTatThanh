        <p  style="text-align: justify;font-size:15px"> <b><i>NTTU -  Sáng ngày 17/09/2019, khoa Công nghệ Sinh học Trường ĐH Nguyễn Tất Thành đã có buổi làm việc với ĐH Hiroshima (Nhật Bản)</i></b> </p>
        <p style="text-align: justify;font-size:15px">Tham dự buổi làm việc có: PGS.TS. Trần Đăng Xuân, Trưởng phòng Sinh lý - Sinh hóa Thực vật ĐH Hiroshima, PGS.TS. Bạch Long Giang, Trưởng phòng Khoa học công nghệ ĐH Nguyễn Tất Thành, PGS.TS. Trần Hoàng Dũng, Trưởng khoa Công nghệ sinh học, PGS.TS. Đặng Thanh Lương, Phó trường khoa Y, phụ trách khối Vật lý – Y sinh cùng quý thầy cô khoa Công nghệ sinh học.</p>
        <p style="text-align: justify;font-size:15px">Tại buổi làm việc, đại diện 2 đơn vị đã giới thiệu khái quát về chương trình đào tạo, hoạt động nghiên cứu và khả năng hợp tác quốc tế của mình. Trong khuôn khổ của buổi làm việc, khoa Công nghệ sinh học cùng PGS.TS. Trần Đăng Xuân chia sẻ về phương hướng hợp tác giữa 2 đơn vị như: trao đổi  tăng cường hợp tác đào tạo và nghiên cứu; Phát triển chương trình đào tạo và chuyển đổi giảng viên, sinh viên. Không chỉ vậy, khoa Công nghệ sinh học Trường ĐH Nguyễn Tất Thành sẽ cử giảng viên, nhiên cứu viên sang trường ĐH Hiroshima để tham gia các khóa tập huấn ngắn hạn 3 tháng về các kỹ thuật chuyên sâu như tách chiết, phân đoạn, giải cấu trúc các hợp chất thiên nhiên trên các trang thiết bị hiện đại bậc nhất của thế giới; kỹ năng viết bài báo khoa học bằng tiếng Anh cho các tạp chí quốc tế.</p>
        <p style="text-align: center;"><img alt="" src="http://ntt.edu.vn/web/upload/images/DSC04625.JPG"  width= "800px" height= "450px"  style="width:100%;"></p>
        <p style="text-align: center;font-size:15px">Đại diện 2 đơn vị cùng nhau trao đổi ý kiến và thống nhất ký kết hợp tác trong thời gian sắp tới</p>
        
        
        <p style="text-align: justify;font-size:15px">Ngoài ra, hai bên cũng đã thống nhất về việc bố trí mời đoàn lãnh đạo Trường ĐH Nguyễn Tất Thành sang thăm và ký kết hợp tác toàn diện với Graduate School of Advanced Technology and Science, đơn vị có chức năng hợp tác Quốc tế và nghiên cứu đỉnh cao của ĐH Hiroshima.</p>
        <p style="text-align: justify;font-size:15px">Tính đến nay, Trường ĐH Nguyễn Tất Thành đã có quan hệ hợp tác bền chặt với nhiều nền giáo dục tiên tiến trên thế giới. Những sự hợp tác này đã mang lại nhiều cơ hội cho giảng viên, sinh viên trường có thể tiếp cận, giao lưu trao đổi học thuật, tích lũy kinh nghiệm về chuyên môn cũng rút ngắn khoảng cách ngôn ngữ. Góp phần giải quyết bài toán thiếu hụt nguồn nhân lực tại các công ty, doanh nghiệp nước ngoài đang hoạt động tại Việt Nam và doanh nghiệp Việt Nam đang hoạt động ở nước ngoài, mở ra cơ hội việc làm cho sinh viên sau khi tốt nghiệp.</p>
