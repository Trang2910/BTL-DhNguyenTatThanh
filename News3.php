        <h3 class="" style="color: #024282 ">ĐH Nguyễn Tất Thành tuyển sinh liên thông các ngành đào tạo</h3>
        <p>Thứ tư, Ngày 11 tháng 09 năm 2019 2:00 pm</p>
        <hr>
        <p style="text-align: justify;font-size:15px"><b>Mở rộng cánh cửa đại học bằng con đường liên thông</b></p>
        <p style="text-align: justify;font-size:15px">Ngày 1/07/2019, Luật Giáo dục đại học sửa đổi 2018 chính thức được thi hành với 9 điều mới. Một trong số những điều mới khiến các sinh viên quan tâm nhất là các loại hình đào tạo đại học sẽ có giá trị như nhau, bằng đại học chính quy, tại chức hay liên thông đều có giá trị tương đồng.</p>
        <p style="text-align: justify;font-size:15px"><b>Cơ hội mới cho sinh viên học liên thông </b></p>
        <p style="text-align: justify;font-size:15px">Quy định mới này đã tạo điều kiện cho các sinh viên theo học các hình thức đào tạo khác nhau khi tốt nghiệp đại học có cơ hội ngang nhau trong việc công nhận văn bằng, tuyển dụng lao động điều này mở ra nhiều cơ hội cho các thí sinh.</p>
        <p style="text-align: justify;font-size:15px">Tại ĐH Nguyễn Tất Thành bên cạnh các chương trình đào tạo chính quy đạt chuẩn kiểm định của Bộ Giáo dục & Đào tạo và được tổ chức QS – Stars Anh Quốc công nhận đạt chuẩn 3 sao thì cơ sở giáo dục này còn được xã hội đánh giá cao về những chương trình đào tạo liên thông từ trung cấp, trung cấp nghề, cao đẳng, cao đẳng nghề lên đại học chính quy.</p>
        <p style="text-align: justify;font-size:15px">Năm 2019, Trường ĐH Nguyễn Tất Thành tuyển sinh Đại học liên thông từ trình độ cao đẳng với 17 ngành gồm: Điều dưỡng, Kỹ thuật Điện, Điện tử, Kế toán, Tài chính ngân hàng; Công nghệ thông tin; Quản trị kinh doanh; Ngôn ggữ Anh; Kỹ Thuật xây dựng; Công Nghệ Thực phẩm; Công nghệ sinh học; Thiết kế đồ họa; Công nghệ kỹ thuật cơ, điện tử; Công nghệ kỹ thuật hóa học; Công nghệ kỹ thuật Ô tô; Dược; Việt Nam học; Ngôn ngữ Trung. Thời gian đào tạo các ngành là 1,5 năm (riêng ngành Dược đào tạo: 2,5 năm)</p>
        <p style="text-align: justify;font-size:15px">Ngay sau khi tốt nghiệp trung cấp, trung cấp nghề, cao đẳng, cao đẳng nghề, thí sinh có thể tham dự kỳ thi tuyển sinh liên thông do trường tổ chức gồm 3 môn: môn cơ bản, môn cơ sở ngành và môn chuyên ngành. Riêng các thí sinh khối ngành Sức khỏe (Dược, Điều dưỡng kèm theo điều kiện phải có chứng chỉ hành nghề). Đây chính là cơ hội, hướng đi mới cho sinh viên dang dở con đường đại học có thể tiếp tục theo đuổi hoàn thiện giấc mơ của mình.</p>
        <p style="text-align: center;"><img alt="" src="http://ntt.edu.vn/web/upload/images/C%C3%B4ng%20ngh%E1%BB%87%20th%C3%B4ng%20tin%20%20%2817%29%281%29.jpg" style="width: 100%" width = "800px" height = "450px"></p>
        <p style="text-align: center;"><span style="color:#A9A9A9;"><em>Liên thông các ngành là cơ hội nâng cao bằng cấp</em></span></p>
        <p style="text-align: justify;font-size:15px">Chương trình đào tạo liên thông của các trường ĐH Nguyễn Tất Thành được xây dựng trên cơ sở chương trình khung của hệ chính quy và được thiết kế phù hợp với các điều kiện đảm bảo chất lượng đào tạo. Góp phần không nhỏ cho sự thành công trong chương trình đào tạo liên thông đại học - cao đẳng- trung cấp của trường đó là đội ngũ những giảng viên giỏi chuyên môn, giàu kinh nghiệm, đảm trách vị trí quản lý và có học vị cao tại các doanh nghiệp. Ngoài nâng cao trình độ chuyên môn, nghiệp vụ nhằm giúp người học thích ứng với môi trường và xu hướng công việc hiện đại, phong cách làm việc năng động, chuyên nghiệp ở các doanh nghiệp chương trình học còn chú trọng đến việc rèn luyện, nâng cao các kỹ năng thực tế mà người sinh viên cần có để hòa nhập bằng các môn kỹ năng ngoại ngữ, tin học và kỹ năng mềm. Các chương trình đào tạo của nhà trường được nhiều giáo sư đầu ngành tham gia thiết kế, bên cạnh đó còn có sự góp ý, phản biện của chính những doanh nghiệp sử dụng lao động, những chuyên gia hàng đầu từ các doanh nghiệp liên kết với nhà trường</p>
        <p style="text-align: justify;font-size:15px"> Điểm nhấn của chương trình liên thông tại ĐH Nguyễn Tất Thành là đào tạo theo học chế tín chỉ giúp người học chủ động trong quá trình học tập. Ngoài ra, thời gian học linh động, chủ yếu học vào buổi tối các ngày trong tuần và thứ bảy, chủ nhật để người học có thể vừa làm vừa học mà vẫn có cơ hội nhận được tấm bằng đại học, cao đẳng chính quy.</p>
        <p style="text-align: justify;font-size:15px"><b>Đào tạo gắn với việc làm -  tuyển sinh gắn với </b></p>
        <p style="text-align: justify;font-size:15px">Không chỉ là ngôi trường đạt chuẩn chất lượng ĐH Nguyễn Tất Thành còn là tòa nhà tri thức hạnh phúc cho SV. Ngôi trường này không chỉ là nơi truyền thụ kiến thức mà là nơi những ước mơ, sáng tạo được thăng hoa, nơi những khát vọng thành công được chắp cánh, nơi những đam mê được khơi dậy. Để làm được điều này, nhà trường đã không ngừng đầu tư cơ sở vật chất, trang thiết bị dạy, học hiện đại, 100% phòng học được trang bị máy lạnh. Hệ thống các dịch vụ hỗ trợ học tập và sinh hoạt từ thư viện, khu tự học, khu vui chơi giải trí cho đến căng - tin luôn mang đến cho SV môi trường thân thiện, phát huy tối đa tinh thần sáng tạo, tư duy và khả năng thích ứng cao.</p>
