        <h3 class="" style="color: #024282 ">Chuẩn 3 sao QS-Stars (Anh Quốc)</h3>
        <p>Chủ nhật, Ngày 19 tháng 02 năm 2017 2:00 pm</p>
        <hr>
        <p id="content1">NTTU - Ngày 22/11/2016, tại Hội nghị Giáo dục Quốc tế QS Apple lần thứ 12 tổ chức tại Malaysia, Trường Đại học Nguyễn Tất Thành đã được trao chứng nhận đạt chuẩn 3 sao theo đánh giá của Tổ chức QS-Stars (Quacquarelli Symonds). </p>
        
        <p id="content1">Chuẩn QS-Stars là chuẩn xếp hạng do Tổ chức Quacquarelli Symonds, Anh Quốc, thực hiện. QS cung cấp hai loại hình đánh giá trường đại học chính, là loại hình Xếp thứ hạng và Gắn sao. Các trường đại học tham gia vào loại hình xếp hạng Gắn sao được xếp theo các cấp bậc phân theo mức độ từ 1 – 5 sao. </p>
        
        <p id="content1">Trường Đại học Nguyễn Tất Thành chính thức trở thành trường đại học thứ 3 tại Việt Nam được công nhận xếp hạng quốc tế 3 sao theo chuẩn QS-Stars, một trong các chuẩn xếp hạng hàng đầu dành cho các trường đại học trên thế giới. </p>
        
        <p id="content1" >Theo đánh giá của QS-Stars, trường đại học được xếp hạng 3 sao là trường có danh tiếng trong nước, thương hiệu quốc tế nhất định; sinh viên tốt nghiệp được các nhà tuyển dụng chào đón. Với Trường Đại học Nguyễn Tất Thành, việc xếp hạng QS-Stars là một sự khẳng định mang tầm quốc tế về chất lượng, danh tiếng, nâng cao giá trị bằng cấp của sinh viên đã và đang theo học tại trường.</p>
        
        <p id="img"<img alt="" src="http://ntt.edu.vn/web/upload/images/Untitled-1%281%29.jpg" style="width: 100%" width = "800px" height = "450px"></p>
        
        <p id="content2" ><em>Bà Mandy Mok - Giám đốc Điều hành QS Stars Châu Á trao&nbsp;chứng nhận đạt tiêu chuẩn quốc tế 3 sao cho Trường ĐH Nguyễn Tất Thành</em></p>
        
        <p id="content3">Tham gia kiểm định theo bảng xếp hạng của QS, Trường ĐH Nguyễn Tất Thành trước đó đã trải qua các vòng kiểm định nghiêm ngặt của tổ chức này trên nhiều tiêu chuẩn, bao gồm: tiêu chuẩn cốt lõi (giảng dạy, việc làm, nghiên cứu khoa học, quốc tế hóa), tiêu chuẩn môi trường học tập (cơ sở vật chất, học tập trực tuyến/ từ xa), tiêu chuẩn chuyên môn (ngành học), tiêu chuẩn nâng cao (trách nhiệm xã hội, tính toàn diện). Trong đó, tiêu chuẩn về giảng dạy, việc làm, cơ sở vật chất, trách nhiệm xã hội được tổ chức QS đánh giá với số điểm tuyệt đối: 5 sao - Mức độ cao nhất trong đánh giá gắn sao của QS. Bên cạnh đó, tiêu chí Tính toàn diện nhận được 4 sao và tiêu chí Nghiên cứu nhận được 2 sao.</p>
        
        <p id="content3">Tại Trường ĐH Nguyễn Tất Thành, chất lượng đào tạo là ưu tiên số một. Qua 20 năm hình thành và phát triển, Trường chưa thực sự có bề dày về “tuổi đời” nhưng đã biết sử dụng hiệu quả sức mạnh nội lực của đội ngũ cán bộ quản lý nhiều kinh nghiệp từ doanh nghiệp; kết hợp với sức mạnh ngoại lực của các chuyên gia đầu ngành về học thuật và nghiên cứu khoa học làm hạt nhân dẫn dắt đội ngũ giảng viên trẻ. Nhà trường liên tục đổi mới, cập nhật kiến thức và phương pháp giảng dạy, ứng dụng thành thạo công nghệ thông tin vào hoạt động giảng dạy thông qua giáo dục số; đầu tư phát triển tài nguyên học liệu số, ebook, thư viện điện tử, giáo trình - bài giảng điện tử; chương trình học mô phỏng thực tế giúp sinh viên có nhiều cơ hội tiếp xúc với thực tế, không còn bỡ ngỡ khi mới ra trường, đáp ứng sự sàng lọc gắt gao của thị trường lao động.</p>
        
        <p id="content3" >Tỷ lệ sinh viên ra trường có việc làm đạt 95% đã góp phần quan trọng tạo nên sự thành công của Nhà trường và sự tín nhiệm của người học. Để đạt được tỷ lệ này, Trường ĐH Nguyễn Tất Thành đã tích cực đẩy mạnh liên kết trong đào tạo giữa nhà trường với doanh nghiệp. Nhà trường thiết lập mô hình CLB Doanh nghiệp với hơn 400 doanh nghiệp thành viên hoạt động đa lĩnh vực. Từ liên kết bền chặt với doanh nghiệp, SV sẽ được lôi cuốn vào thực tiễn công việc, được trải nghiệm trong môi trường trẻ trung, sôi động và thách thức của doanh nghiệp. Trường ĐH Nguyễn Tất Thành đã ký kết hợp tác toàn diện với các cơ sở như: Nhà máy sản xuất dược phẩm, trung tâm kiểm nghiệm thuốc, công ty kinh doanh và phân phối thuốc, bệnh viện, Công ty CP Sữa Việt Nam Vinamilk nhằm tạo điều kiện tốt nhất cho SV được học tập và làm việc ngay khi còn trên giảng đường.</p>
        
        <p id="content1" ><img alt="" src="http://ntt.edu.vn/web/upload/images/26380243870_c2473ff213_Q.8.jpg" style="width: 100%" width = "800px" height = "450px"></p>
        
        <p id="content2"><em>Lễ ký kết hợp tác giữa Đại học Nguyễn Tất Thành và BV Quận 8&nbsp;</em><em>về việc&nbsp;đảm bảo&nbsp;cơ hội thực tập và làm việc cho 30 SV của trường tại&nbsp;</em></p>
        
        <p id="content3">Về cơ sở vật chất, Trường ĐH Nguyễn Tất Thành đầu tư hơn 2.000 tỷ đồng phát triển cơ sở vật chất, Nhà trường đã xây dựng 100.000m2 sàn phòng học, trang bị 3.000 máy tính và mua sắm trang thiết bị dạy học hiện đại, đầu tư phát triển thư viện, 100% phòng học lý thuyết, phòng thực hành, thí nghiệm được trang bị máy lạnh và phủ sóng WiFi.</p>
        
        <p id="content3">Với truyền thống khuyến học, khuyến tài và tâm niệm thực hiện mong muốn của Bác Hồ là “Ai cũng có cơm ăn, áo mặc, ai cũng được học hành”, Nhà trường ý thức được vai trò và trách nhiệm đối với xã hội trong việc chung tay đóng góp tạo điều kiện cho các em học sinh trên mọi miền đất nước được tiếp tục học tập, thực hiện những ước mơ, hoài bão của mình. Để thực hiện điều đó, hằng năm, Trường ĐH Nguyễn Tất Thành đã dành tặng hàng tỉ đồng học bổng cho các em học sinh – sinh viên dưới nhiều hình thức khác nhau.</p>
        
        <p id="content3">Về tiêu chí nghiên cứu (Research), mặc dù Trường ĐH Nguyễn Tất Thành đạt 2 sao, còn ở mức thấp nhưng quá trình nghiên cứu khoa học của Nhà trường đã có nhiều nỗ lực. Tính đến nay, Trường đã thực hiện gần 1.000 đề tài nghiên cứu khoa học các cấp, trong đó có 25 đề tài cấp nhà nước, 27 đề tài cấp bộ, 22 đề tài cấp sở, 04 đề tài hợp tác quốc tế, trên 350 đề tài cấp trường và trên 500 đề tài nghiên cứu khoa học của sinh viên; và công bố 356 bài báo ISI/SCOPUS, hơn 600 bài báo đã đăng trên các tạp chí trong và ngoài nước. Hàng năm, Nhà trường trích hàng chục tỷ đồng ngân sách phục vụ công tác nghiên cứu khoa học của cán bộ giảng viên, nhân viên, sinh viên, nhằm phấn đấu trở thành một trong những trường đại học ứng dụng – thực hành hàng đầu Việt Nam. Từ đó, góp phần đưa Trường ĐH Nguyễn Tất Thành sánh vai với các trường đại học trong khu vực và quốc tế. Thời gian tới, Nhà trường sẽ tiếp tục chú trọng đầu tư vào phát triển hoạt động nghiên cứu này.</p>
        
        <p id="content3">Đợt đánh giá QS-Stars đầu tiên của Trường ĐH Nguyễn Tất Thành đã giúp Nhà trường khẳng định được uy tín của mình trên bản đồ giáo dục quốc tế. Thông qua đó, Trường ĐH Nguyễn Tất Thành nhận thức rõ ràng những điểm mạnh và điểm còn hạn chế dựa trên những tiêu chuẩn đánh giá khách quan, công bằng. Từ đó, tập thể Nhà trường tiếp tục nỗ lực nhằm nâng cao chất lượng trên các mặt, phục vụ tốt nhất công tác đào tạo nguồn nhân lực chất lượng cao, hội nhập vào nền giáo dục khu vực và quốc tế.</p>
        
        <p id="content3">Nhân dịp này, Cục Khảo thí và Kiểm định Chất lượng Giáo dục đã đánh giá cao những nỗ lực và kết quả đạt được của Trường ĐH Nguyễn Tất Thành đối với công tác đảm bảo và kiểm định chất lượng trong thời gian qua. Trường ĐH Nguyễn Tất Thành không chỉ là một trong ba trường đầu tiên của Việt Nam đã được xếp hạng 03 sao của QS-Stars mà còn đăng kí làm thành viên liên kết của Mạng lưới các trường đại học ASEAN (AUN). Hơn thế nữa, Trường ĐH Nguyễn Tất Thành là trường đại học ngoài công lập đầu tiên được thẩm định báo cáo tự đánh giá và sẽ được đánh giá ngoài bởi Trung tâm Kiểm định chất lượng giáo dục - ĐH Quốc gia TP.HCM vào cuối năm 2016. Đến năm 2017 thì được chứng nhận đạt chuẩn chất lượng giáo dục của Bộ Giáo dục và Đào tạo.</p>
        



